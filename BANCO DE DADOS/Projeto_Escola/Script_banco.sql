CREATE DATABASE escola_abc;
USE escola_abc;

-- Cadastro de ALUNOS
CREATE TABLE aluno (
    id_aluno INT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(50),
    idade INT,
    sexo CHAR(1),
    telefone VARCHAR(25),
    endereco VARCHAR(100),
    data_matricula DATE,
    mensalidade FLOAT(10,2),
    ativo_sn INT
);

-- Cadastro de PROFESSORES
CREATE TABLE professor (
    id_professor INT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(50),
    idade INT,
    sexo CHAR(1),
    telefone VARCHAR(25),
    endereco VARCHAR(100),
    data_admissao DATE,
    salario FLOAT(10,2),
    area VARCHAR(50),
    ativo_sn INT
);

